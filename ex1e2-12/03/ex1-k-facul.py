# A função range(10, -1, -1) gera uma sequência de números começando de 10 até 0, indo de trás para frente.
# O terceiro argumento (-1) indica que o valor é decrementado a cada iteração.
for i in range(10, -1, -1):  # A contagem começa em 10 e termina em 0.
    print(i)  # Imprime o valor de i a cada passo

